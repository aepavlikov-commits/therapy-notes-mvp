export function detectRiskFlag(clinicalNoteText) {
  const noRiskPhrase = "признаков риска в данной сессии не зафиксировано";
  const lower = clinicalNoteText.toLowerCase();
  if (lower.includes("⚠ риск") || lower.includes("риск")) {
    return !lower.includes(noRiskPhrase);
  }
  return false;
}
