function buildModalityBlock(modalities) {
  if (!modalities || modalities.length === 0) return "";
  const hints = modalities
    .map((m) => MODALITY_HINTS[m])
    .filter(Boolean);
  if (hints.length === 0) return "";
  return `\nТерапевт работает в следующих подходах (это влияет только на то, какую терминологию и какие детали из транскрипта стоит точнее отразить — НЕ добавляй интерпретаций или техник, которых не было в транскрипте):\n${hints.map((h) => "- " + h).join("\n")}\n`;
}

export function buildClinicalPrompt(transcript, format, customInstructions, modalities) {
  let structureBlock;
  if (format === "CUSTOM" && customInstructions) {
    structureBlock = `Структура заметки (задана терапевтом):\n${customInstructions}`;
  } else {
    structureBlock = FORMAT_STRUCTURES[format] || FORMAT_STRUCTURES.SOAP;
  }

  const modalityBlock = buildModalityBlock(modalities);

  return `Ты — ассистент психотерапевта, который готовит ЧЕРНОВИК КЛИНИЧЕСКОЙ ЗАМЕТКИ после сессии, СТРОГО для самого терапевта.
${RISK_RULES}
${modalityBlock}
${structureBlock}

Транскрипт сессии:
---
${transcript}
---

Составь черновик клинической заметки.`;
}

