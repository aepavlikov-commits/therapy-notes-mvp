// /lib/prompts.js
// Промпты для генерации заметок. Поддерживает несколько стандартных форматов
// плюс пользовательский формат, заданный самим терапевтом.

const RISK_RULES = `
КРИТИЧЕСКИ ВАЖНЫЕ ПРАВИЛА:
1. Ты ТОЛЬКО извлекаешь и структурируешь факты из транскрипта. Ты НЕ ставишь клинические гипотезы, НЕ формулируешь "ядерные убеждения", НЕ строишь концептуализацию личности — это делает сам терапевт.
2. Если в транскрипте есть ЛЮБЫЕ упоминания суицидальных мыслей, планов, самоповреждения, употребления веществ с целью вреда себе — вынеси это в отдельный блок "⚠ РИСК" в самом начале заметки, используя по возможности ПРЯМЫЕ ЦИТАТЫ клиента. Не смягчай и не интерпретируй эти фразы. Если рисков не было — напиши "Признаков риска в данной сессии не зафиксировано".
3. Если в транскрипте упоминаются ЛЕКАРСТВА или ДОЗИРОВКИ — копируй их БУКВАЛЬНО, как сказал терапевт. Никогда не придумывай и не корректируй дозировки сам.
`;

const FORMAT_STRUCTURES = {
  SOAP: `
Структура SOAP:
- ⚠ РИСК (если применимо)
- S (Subjective) — состояние клиента со слов клиента
- O (Objective) — наблюдаемые факты, поведение, темы сессии
- A (Assessment) — оставь как "[Заполняется терапевтом]" — это требует клинического суждения
- P (Plan) — план/домашнее задание, договорённости на следующую сессию`,

  DAP: `
Структура DAP:
- ⚠ РИСК (если применимо)
- D (Data) — что происходило на сессии: темы, состояние клиента, факты
- A (Assessment) — оставь как "[Заполняется терапевтом]"
- P (Plan) — план на следующую сессию, домашнее задание`,

  BIRP: `
Структура BIRP:
- ⚠ РИСК (если применимо)
- B (Behavior) — наблюдаемое поведение и слова клиента
- I (Intervention) — какие техники/интервенции использовал терапевт (фактологически)
- R (Response) — как клиент отреагировал на интервенции
- P (Plan) — план на следующую сессию`,

  GIRP: `
Структура GIRP:
- ⚠ РИСК (если применимо)
- G (Goal) — над какой целью работали на сессии
- I (Intervention) — техники терапевта
- R (Response) — реакция клиента
- P (Plan) — план на следующую сессию`,
};

export function buildClinicalPrompt(transcript, format, customInstructions) {
  let structureBlock;
  if (format === "CUSTOM" && customInstructions) {
    structureBlock = `Структура заметки (задана терапевтом):\n${customInstructions}`;
  } else {
    structureBlock = FORMAT_STRUCTURES[format] || FORMAT_STRUCTURES.SOAP;
  }

  return `Ты — ассистент психотерапевта, который готовит ЧЕРНОВИК КЛИНИЧЕСКОЙ ЗАМЕТКИ после сессии, СТРОГО для самого терапевта.
${RISK_RULES}
${structureBlock}

Транскрипт сессии:
---
${transcript}
---

Составь черновик клинической заметки.`;
}

export function buildClientPrompt(transcript) {
  return `Ты — ассистент психотерапевта, который готовит ПАМЯТКУ ДЛЯ КЛИЕНТА после сессии.

СТРОГИЕ ПРАВИЛА:
1. Простой, тёплый, человеческий язык. Без клинических терминов.
2. Если в транскрипте упоминаются ЛЕКАРСТВА или ДОЗИРОВКИ — копируй их БУКВАЛЬНО, как сказал терапевт. Если дозировок в транскрипте нет — не упоминай медикаменты вообще.
3. Не пиши ничего, чего не было в транскрипте.
4. Структура: тёплое вступление, "Что мы обсудили", "Договорённости на случай тяжёлого момента" (если был план безопасности), "Домашнее задание".

Транскрипт сессии:
---
${transcript}
---

Напиши памятку для клиента.`;
}

export function detectRiskFlag(clinicalNoteText) {
  const noRiskPhrase = "признаков риска в данной сессии не зафиксировано";
  const lower = clinicalNoteText.toLowerCase();
  if (lower.includes("⚠ риск") || lower.includes("риск")) {
    return !lower.includes(noRiskPhrase);
  }
  return false;
}
